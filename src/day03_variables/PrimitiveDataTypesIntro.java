package day03_variables;

public class PrimitiveDataTypesIntro {

    public static void main(String[] args) {

        // byte a = (-128~127) integer
        // byte a = "java" byte only accepts integer numbers X
        byte a = 20;                

        // short 6

        short p = 17500;

        System.out.println(p);

        int s = 950000;

        System.out.println(s);

        long n = 999999365999235235L;

        System.out.println(n);

        //--------------------------------------

        // float and double

        float b = 343.4436F;

        System.out.println(b);

        double c = 4562353.785356434;

        System.out.println(c);

        //----------------------------------------

        //char and boolean

        char d = 0;
        char e = '&';

        System.out.println(e);

        System.out.println(d);

        boolean ImDeveloper = true;

        System.out.println(ImDeveloper);

        boolean funny = 803456 > 451236;

        System.out.println(funny);

        //-----------------------------------------

        //
        int fd = 'A' + 'B';
        System.out.println(fd);


            }
}
