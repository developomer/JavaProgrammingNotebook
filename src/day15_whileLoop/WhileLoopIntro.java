package day15_whileLoop;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if (true){
            System.out.println("Hello My Planet");
        }

        System.out.println("------------------");

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        
        
        System.out.println("-------------------");

        while (true){
            System.out.println("Hello My Planet");
            break;
        }







    }

}
